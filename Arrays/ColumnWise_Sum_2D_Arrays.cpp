#include <iostream>
using namespace std;

void sum(int arr[][4])
{
    
    cout << "Printing Sum" << endl;

    for (int col = 0; col < 4; col++)
    {
        int sum = 0;
        for (int row = 0; row < 3; row++)
        {
            sum += arr[row][col];
        }
        cout << sum << " ";
    }
}

